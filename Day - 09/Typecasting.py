a = "1"
b = "2"
print("Concatenation of string : ", a + b)

# Explicit TypeCasting --> Done by User
print("Sum of numbers a and b  : ", int(a) + int(b))

string = "15"
number = 7
string_number = int(string)
suma = number + string_number
print("Sum is ", suma)

# Implicit TypeCasting --> Done by python compiler
c = 1.9
d = 8
print("Sum of c and d is ", c + d)
